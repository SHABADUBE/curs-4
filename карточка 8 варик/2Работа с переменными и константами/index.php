<?php
// Задаем стоимость тетради и обложки
$стоимость_тетради = 50; // предположим, стоимость одной тетради в рублях
$стоимость_обложки = 20; // предположим, стоимость одной обложки в рублях

// Задаем количество тетрадей и обложек
$количество_тетрадей = 3;
$количество_обложек = 3;

// Вычисляем общую стоимость покупки
$итоговая_стоимость = ($стоимость_тетради * $количество_тетрадей) + ($стоимость_обложки * $количество_обложек);

// Выводим результат на экран
echo "Итоговая стоимость покупки: " . $итоговая_стоимость . " рублей";
?>
